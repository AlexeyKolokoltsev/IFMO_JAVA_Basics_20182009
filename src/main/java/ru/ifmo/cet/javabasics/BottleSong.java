package ru.ifmo.cet.javabasics;



public class BottleSong {

    private final int bottleT;

    public BottleSong(int bottleT) {
        this.bottleT = bottleT;
    }

    public String getBottleSongLyrics() {
        if(this.bottleT <1 || this.bottleT > 99) throw new IllegalArgumentException();

        String txt = "";
        for(int i = 99; i > 0; i -= bottleT){
            txt += i + ((i == 1) ? " bottle" : " bottles") + " of beer on the wall, " + i + ((i  == 1) ? " bottle" : " bottles") + " of beer.\n" +
                    "Take " + ((i > bottleT)? NumberToWords.convert(bottleT) : NumberToWords.convert(i) )
                    + " down and pass around, " + ((i - bottleT > 0) ? (i - bottleT) : "no more")
                    + ((i - bottleT == 1) ? " bottle" : " bottles") + " of beer on the wall.\n";
        }

        txt += "No more bottles of beer on the wall, no more bottles of beer.\n" +
                "Go to the store and buy some more, 99 bottles of beer on the wall.\n";

        return txt;

    }
}


class NumberToWords {


    final private  static String[] units = {"zero","one","two","three","four",
            "five","six","seven","eight","nine","ten",
            "eleven","twelve","thirteen","fourteen","fifteen",
            "sixteen","seventeen","eighteen","nineteen"};
    final private static String[] tens = {"","","twenty","thirty","forty","fifty",
            "sixty","seventy","eighty","ninety"};


    public static String convert(Integer i) {
        if( i < 20)  return units[i];
        return tens[i/10] + ((i % 10 > 0)? " " + convert(i % 10):"");
    }

}

































